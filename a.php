<div class="entry-content" itemprop="articleBody">
<br />
<figure class="wp-block-image"><img loading="lazy" width="788" height="525" src="https://i0.wp.com/www.jengana.co.id/wp-content/uploads/2019/08/Kayu-Bajakah.jpg?resize=788%2C525&#038;ssl=1" alt="Para penjual kayu Bajakah pun beragam menjual harga dengan ukuran potongan kayu Bajakah bervariasi pula. Bahkan bukan hanya kayu nya, air dari Bajakah pun di jual dalam kemasan botol bekas air mineral pun bervariasi harganya dari setiap penjualnya." class="wp-image-4876" srcset="https://i0.wp.com/www.jengana.co.id/wp-content/uploads/2019/08/Kayu-Bajakah.jpg?w=800&amp;ssl=1 800w, https://i0.wp.com/www.jengana.co.id/wp-content/uploads/2019/08/Kayu-Bajakah.jpg?resize=300%2C200&amp;ssl=1 300w, https://i0.wp.com/www.jengana.co.id/wp-content/uploads/2019/08/Kayu-Bajakah.jpg?resize=768%2C512&amp;ssl=1 768w, https://i0.wp.com/www.jengana.co.id/wp-content/uploads/2019/08/Kayu-Bajakah.jpg?resize=750%2C500&amp;ssl=1 750w" sizes="(max-width: 788px) 100vw, 788px" data-recalc-dims="1" /><figcaption><strong><em>Kayu Bajakah</em></strong> <em>Property Image : liputansbm.com </em></figcaption></figure>
<p>Tiga siswa SMAN 2 Palangkaraya, Kalimantan Tengah, mendadak tenar. Sebabnya, karena mereka berhasil menemukan tumbuhan yang bisa dijadikan obat penyembuh kanker, yakni kayu Bajakah.</p>
<p>Tentu saja, kita semua patut bangga, sebab temuan ketiga siswa tersebut menjadi juara dalam ajang lomba ilmiah internasional World Invention Creativity Olympic (WICO) 2019 di Korea Selatan. Mereka diganjar hadiah utama, medali emas.</p>
<p>Berita terkait kayu Bajakah yang digadang-gadang sebagai obat kanker ini tentu saja membuka mata kita akan kekayaan tanaman herbal Nusantara. Janganlah hal ini menjadi polemik yang seakan akan menihilkan prestasi ketiga siswa tersebut.</p>
<p>“Meskipun belum teruji secara klinis, namun secara empirik kayu Bajakah tersebut sudah cukup terbukti memiliki khasiat membantu penyembuhan. </p>
<p>Kepercayaan ini sudah berakar secara turun temurun, khususnya di kalangan masyarakat Dayak di Kalimantan Tengah,” ungkap Jeng Ana, herbalis yang sudah banyak malang melintang di dunia pengobatan alternative, khususnya pemanfaatan tanaman obat Nusantara.</p>
<p>Jeng Ana mengaku sangat mengapresiasi prestasi yang dicapai oleh ketiga siswa SMAN 2 Palangkaraya tersebut. Ia juga tidak menutup kemungkinan akan menggunakan kaya Bazakah dalam racikan herbalnya, khususnya untuk membantu penyembuhan kanker.</p>
<p>“Selama ini, dalam membantu penyembuhan kanker saya sudah menggunakan jenis kayu Ular dari Papua. Ke depan, jika memang bahan stok bakunya memadai, tidak menutup kemungkinan saya akan gunakan kayu Bajakah,” tegas Jeng Ana.</p>
<p>Dengan kehebohan pemberitaan terkait kayu Bajakah, Jeng Ana secara pribadi berharap agar penggunaan obat tradisional, khususnya herbal, akan semakin diperkuat lagi.</p>
<p>Menurut herbalis yang sudah ancang-ancang memproduksi sejumlah obat herbal ini, selain dihadang formalitas bidang kesehatan dan uji klinik, perkembangan obat tradisional juga dihadapkan pada kompetisi bisnis yang sangat ketat. </p>
<p>“Bagaimanapun obat-obat modern dibuat oleh perusahaan farmasi berskala raksasa, baik perusahaan asing maupun dalam negeri, yang sudah sangat berpengalaman dan menguasai pasar dengan berbagai strateginya. </p>
<p>Sedangkan obat tradisinal seperti herbal, masih berada pada skala industri kecil. Karena itu menurut saya pemerintah harus memberikan perhatian khusus,” sambung Jeng Ana.</p>
<p>Dibalik beragam kendala, Jeng Ana tetap meyakini banyak celah yang dapat dimanfaatkan untuk pengembangan tanaman obat, sebagai bahan baku obat herbal. </p>
<p>“Dengan hadirnya obat-obatan herbal, kebutuhan bidang kesehatannya, masyarakat menjadi punya banyak pilihan. Bisa memilih obat modern atau obat tradisiona,” tegas Jeng Ana. </p>
<p>Dalam hal ini pemahaman masyarakat mengenai khasiat tanaman obat tertentu perlu ditingkatkan. Untuk mendukung hal ini, upaya yang ditempuh Jeng Ana adalah melalui media cetak dan media elektronik. </p>
<p>“Artikel maupun wawancara saya diberbagai media merupakan suatu usaha pribadi saya untuk terus menumbuh-kembangkan pengetahuan dan minat masyarakat terhadap pengobatan herbal. </p>
<p>Karena itu saya berharap, jangan ada aturan aturan yang mempersulit pengobatan alternatif untuk tampil di media massa, khususnya elektronik,” harap Jeng Ana sekaligus menutup perbincangan santai dengan media ini. (*)</p>
